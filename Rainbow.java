
public class Rainbow {

	public static void main(String[] args) {

		String a = "Yellow";
		switch (a) {
		case "Violet":
			System.out.println("Violet is a rainbow colour");
			break;

		case "Indigo":
			System.out.println("Indigo is a rainbow colour");
			break;

		case "Blue":
			System.out.println("Blue is a rainbow colour");
			break;

		case "Green":
			System.out.println("Green is a rainbow colour");
			break;

		case "Yellow":
			System.out.println("Yellow is a rainbow colour");
			break;

		case "Red":
			System.out.println("Red is a rainbow colour");
			break;
		}

	}

}
